"""Estructura de datos"""

"""lis.reverse -> revierte los valores de una lista"""

datosPersona = ["Luis", "Gonzales", 30, "20/08/1992"]
print("Los datos de la lista son: {}".format(datosPersona))

datosPersona.reverse() #invierte los elementos de una lista

print("Los datos actualizados de la lista son: {}".format(datosPersona))
